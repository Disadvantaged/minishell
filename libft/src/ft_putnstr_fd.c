#include "libft.h"

int		ft_putnstr_fd(char *s, int n, int fd)
{
	int		i;

	if (s == NULL)
		return (0);
	i = 0;
	while (s[i] && i < n)
	{
		ft_putchar_fd(s[i++], fd);
	}
	return (i);
}
