#include "libft.h"

int		ft_atoi(const char *s)
{
	int		sign;
	int		i;
	int		n;

	i = 0;
	sign = 1;
	while (s[i] == ' ' || s[i] == '\t'
			|| s[i] == '\n' || s[i] == '\v' || s[i] == '\r' || s[i] == '\f')
		i++;
	if (s[i] == '+' || s[i] == '-')
	{
		if (s[i] == '-')
			sign *= -1;
		i++;
	}
	n = 0;
	while (s[i] >= '0' && s[i] <= '9')
	{
		n = n * 10 + s[i] - 48;
		i++;
	}
	return (n * sign);
}
